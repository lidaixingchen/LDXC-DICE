export function getValidationRulesByTable(tableName: string): ValidationRuleConfig[] {
  return AIDM_VALIDATION_PRESET.preset.rules.filter(rule => rule.targetTable === tableName);
}

export function getValidationRuleById(id: string): ValidationRuleConfig | undefined {
  return AIDM_VALIDATION_PRESET.preset.rules.find(rule => rule.id === id);
}

export function getEnabledValidationRules(): ValidationRuleConfig[] {
  return AIDM_VALIDATION_PRESET.preset.rules.filter(rule => rule.enabled);
}
